package org.techtown.interfaceprograms;

public class Country {
    private double area;
    private String country;

    public Country() {
        this.country = country;
        this.area = area;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public void setCountry(String country) {
        this.country = country;
    }
}
